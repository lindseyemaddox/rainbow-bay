    <p class="faquestion"><span class="icon-question"></span> Is there a computer for personal use?</p>

    <p class="faqanswer">Yes there is a computer you can use at the lodge for e-mail. Make sure you know how to get to your e-mail server from a computer other than yours! The computer at the lodge does have this capability. Make sure your digital camera has a large memory card (2 Gig or bigger), which will allow you to take a large number of high resolution pictures during your stay at the lodge.</p>

    <p class="faquestion"><span class="icon-question"></span> What about liquor?</p>

    <p class="faqanswer">The lodge provides a complimentary selection of beer and wines for your convenience. If you desire liquor (hard stuff) you will need to bring that and any mixers you desire.</p>

    <p class="faquestion"><span class="icon-question"></span> Do you offer trip insurance?</p>

    <p class="faqanswer">We do not offer trip insurance, but it something to consider. Our policy is that trip deposits, balances and airline tickets paid are not refundable unless we can find a replacement angler. For trip insurance info go to Google.com on your computer and type in “Trip Insurance” as the search words. You will find a number of companies that offer trip insurance you can purchase online. They offer a variety of options of what you can and can’t insure on a trip.</p>

    <p class="faquestion"><span class="icon-question"></span> What time are meals served?</p>

    <p class="faqanswer">Breakfast will be ready at 7:15 am and dinner is served at approximately 7:30 pm. We cook shore lunches when possible, if not you will be furnished with a sack lunch.</p>

    <p class="faquestion"><span class="icon-question"></span> Does the lodge have a washer and dryer?</p>

    <p class="faqanswer">Yes, the lodge staff will do laundry for you as needed.</p>

    <p class="faquestion"><span class="icon-question"></span> What about fishing licenses?</p>

    <p class="faqanswer">The lodge is an Alaska license vendor and you will purchase your license upon your arrival. A 7 day non-resident fishing license is $55.00, should you fish for King Salmon in June or July you will also need to buy a $35.00 King stamp. Children under 15 years of age do not require a license.</p>

    <p class="faquestion"><span class="icon-question"></span> What are the fishing hours?</p>

    <p class="faqanswer">Approximately 8:00 am – 5:00 pm</p>

    <p class="faquestion"><span class="icon-question"></span> How many fishermen per guide?</p>

    <p class="faqanswer">Generally we provide one guide per each two fishermen. Occasionally on a fly out, the pilot will guide 3 fishermen.</p>

    <p class="faquestion"><span class="icon-question"></span> What are the catch limits?</p>

    <p class="faqanswer">The catch limit varies each season depending upon the strength of the Salmon runs. We practice catch and release on the Rainbow, Arctic Char Grayling & Halibut. You can keep 2 – 5 Salmon & 2 Halibut per day depending on the species and the stream in which we are fishing.</p>

    <p class="faquestion"><span class="icon-question"></span> What if I want to have a fish mounted?</p>

    <p class="faqanswer">Again we practice catch and release on everything but salmon & Halibut. Should you want to have a mount made of a particular fish, we recommend a fiberglass “Fish Clone” mount. You simply need to take a picture of your fish, measure the length & the taxidermist can make the Fish Clone to match the photograph. The old skin type mounts deteriorate in just a few years where the new Fish Clones look good & last virtually forever.</p>

    <p class="faquestion"><span class="icon-question"></span> Smoking and/or canning of fish?</p>

    <p class="faqanswer">Anchorage has several companies that will provide this service and will ship the products to you.</p>

    <p class="faquestion"><span class="icon-question"></span> What about getting our fish home?</p>

    <p class="faqanswer">The lodge provides a wax cardboard box especially made for this purpose. Each day your guide will clean and freeze your salmon &/or halibut and at the end of your trip your box of filets will be frozen and ready for your trip home. The box holds 30 – 40 lbs of filets. There is usually a $25.00 charge on the charter flight for each fish box.</p>

    <p class="faquestion"><span class="icon-question"></span> What is the customary tip for guides and staff at the lodge?</p>

    <p class="faqanswer">Tipping is optional at Rainbow Bay Resort (this is not the case at some lodges). Our staff pools all the tips received and divides them between all the staff. A recent article in the Alaska Angling Guide suggests $500.00/guest as a guideline for gratuities to Alaska Sport Fishing Lodge staffs for a satisfied guest. This is representative of the average tip left by our guest in the past. You may leave your gratuity with any of the lodge management; it will be greatly appreciated by the staff at the lodge.</p>

