	<h2>Frequently Asked Questions</h2>

	<p class="faquestion"><span class="icon-question"></span> Do you have phone service at the lodge?</p>

        <p class="faqanswer show">Yes, we provide satellite phone service for our guests. Because guests are charged for using this service, we recommend that they bring prepaid calling cards to get the best rates.</p>

        <p class="faquestion"><span class="icon-question"></span> Is there a computer that I may use to check email? What about transferring images from my camera to a CD or flash drive?</p>

        <p class="faqanswer">Yes, guests are welcome to use our computer to check email or transfer pictures. Just make sure you know how to access your email from a computer other than yours.</p>

        <p class="faquestion"><span class="icon-question"></span> Does the lodge provide liquor?</p>

        <p class="faqanswer">We provide a complimentary selection of beer and wine. If you’re looking for the “hard stuff,” you’ll need to bring that, and any mixers, with you.</p>

        <p class="faquestion"><span class="icon-question"></span> Do you offer trip insurance?</p>

        <p class="faqanswer">We do not offer trip insurance. Our policy is that trip deposits, balances, and airline tickets are not refundable unless we can find someone else to replace your spot. If you’re interested in trip insurance, we recommend researching information online to find an option that best suits your needs.</p>

        <p class="faquestion"><span class="icon-question"></span> What time are meals served?</p>

        <p class="faqanswer">Breakfast will be ready at 7:15 a.m. AKST, and dinner is served at approximately 7:30 p.m. AKST. We cook shore lunches when the weather and location allow. Otherwise, we’ll provide you with a sack lunch.</p>

        <p class="faquestion"><span class="icon-question"></span> What kind of meals are served?</p>

        <p class="faqanswer">Guests are served three meals a day. Breakfast is typically served around 7:15 a.m. Lunch will either be a shore-cooked lunch or a prepared sack lunch depending on the fishing destination and weather. After guests return to the lodge, they are treated to hors ‘dourves, beer, and wine followed by an upscale dinner that may include king crab, prime rib, or inch-thick pork chops, just to name a few.</p>

        <p class="faquestion"><span class="icon-question"></span> Does the lodge provide laundry service?</p>

        <p class="faqanswer">Yes, we’ll do laundry for you as needed.</p>

        <p class="faquestion"><span class="icon-question"></span> What about fishing licenses?</p>

        <p class="faqanswer">Because we’re an Alaskan License Vendor, you can simply purchase your fishing license upon your arrival at our lodge. A seven-day, non-resident fishing license is $55. If you fish for king salmon in June or July, you will also need a $35 king stamp. Children under 15 years of age do not require a license.</p>

        <p class="faquestion"><span class="icon-question"></span> How much time will I spend fishing each day?</p>

        <p class="faqanswer">This depends on your fishing destination and the weather, but during a typical day you’ll be fishing from 8 a.m. to 5 p.m. AKST.</p>

        <p class="faquestion"><span class="icon-question"></span> Can I rent waders from Angry Eagle? Or, can you order a pair for me?</p>

        <p class="faqanswer">Yes, you can rent high-quality Simms® G3 and Patagonia® waders and wading boots for $100 weekly. We’re also happy to purchase Simms or Patagonia waders and boots for you and have them waiting at the lodge upon your arrival. If you’d like to rent or purchase waders and boots, please <a href="/contact">let us know</a> at least 30 days prior to your arrival.</p>

        <p class="faquestion"><span class="icon-question"></span> How many fishermen are placed per guide?</p>

        <p class="faqanswer">Generally, we provide one guide per two fishermen. Occasionally on a fly out, the pilot/guide will assist three fishermen.</p>

        <p class="faquestion"><span class="icon-question"></span> What are the catch limits?</p>

        <p class="faqanswer">The catch limit varies each season, depending upon the strength of the salmon runs. You can keep two to five salmon and two halibut per day, depending on the species and the stream in which we are fishing. We practice catch and release on everything but salmon and halibut.</p>

        <p class="faquestion"><span class="icon-question"></span> What if I want to have a fish mounted?</p>

        <p class="faqanswer">Again, we practice catch and release on everything but salmon and halibut. But, if you want to have a mount of a particular fish made, we recommend a fiberglass “fish clone” mount. Simply take a picture of your fish and measure its length. A taxidermist will create a long-lasting clone that matches the photograph.</p>

        <p class="faquestion"><span class="icon-question"></span> What if I want my fish smoked or canned?</p>

        <p class="faqanswer">Anchorage has several companies that provide this service and will ship the finished products to you.</p>

        <p class="faquestion"><span class="icon-question"></span> How do I get my fish home?</p>

        <p class="faqanswer">Each day, we’ll fillet your salmon and halibut and then vacuum pack, freeze, and place in it a travel-friendly box that holds up to 40 pounds of fish. There’s a $25 charge for each box of fish on your Lake Clark Air flight to Anchorage. Once you’re at the Anchorage International Airport, check your fish in their walk-in freezer while you wait for your flight home. When your departure time draws near, simply claim your box from the airport freezer and check it on your flight as baggage. Your fish should remain frozen until you arrive at your final destination.</p>

        <p class="faquestion"><span class="icon-question"></span> What is the customary tip for guides and staff at the lodge?</p>

        <p class="faqanswer">While optional at Rainbow Bay Resort, tips are greatly appreciated. Typically, our guests each tip $500 per stay. You may leave your gratuity with any member of the lodge management, who will pool and divide it among all staff members.</p>

