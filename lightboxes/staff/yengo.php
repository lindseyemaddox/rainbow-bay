  	<p>When you love fishing as much as Tom Yengo does, it’s only natural to want to share your passion with others. And for him, there's nothing better than seeing the excitement on a client's face as his or her line tightens.</p>

	<p>“Fishing is just good for your soul,” Tom said. “My dad instilled his love of fishing in me when I was young. He also gave me the ability to teach others the art of casting a line.”</p>

	<p>Guiding is more than a job for Tom. It’s about having a great time while sharing the experience of catching fish. And whether he’s instructing someone new to the sport or catering to an experienced angler, Tom is focused on helping clients land the perfect catch.</p>

	<p>“I know that when I take people fishing, it’s about more than catching fish,” said Tom, who’s been a fishing guide for more than 20 years. “For many guests, it’s a trip of a lifetime. That’s why I try to make every trip an unforgettable adventure.”</p>

	<p>He’s also a boat captain and holds a 100 Ton Masters Merchant Marine license. He even piloted a 55-ft. yacht from Seattle to Homer, Alaska, 1,900 miles up the Inside Passage and Gulf of Alaska.</p>

	<p>In the off season, Tom lives near the Gunnison River in Colorado and often fishes for salmon and trout. As a California native, he also loves fishing off San Diego’s coast, where he once battled a 350-lb. swordfish for six hours before it got away just inches from his boat. And when he’s not on the water, you’ll find him spending time with his family, perfecting the bass guitar, or snow skiing.</p>

