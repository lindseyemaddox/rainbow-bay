  <h1>Jim Kern</h1>

  <h2>Operations Manager</h2>

  <img src="/_assets/img/staff-kern.jpg" alt="Rainbow Bay Operations Manager Jim Kern">

	<p>Whether it’s overseeing Rainbow Bay Resort’s sales and marketing, keeping things running smoothly at both lodges, or introducing guests to the wilds of Alaska, it’s safe to say that Jim Kern is one busy—and diverse—guy.</p>

	<p>Born and raised in central Michigan, Jim grew up hunting and fishing before attending Delta College and Michigan State University. From there, he enjoyed a career many dream about, but only a few get to experience. </p>

	<p>For nearly 20 years, he played professional baseball for six Major League teams and was selected to American League All-Star teams in 1977,1978, and 1979. He also won the Rolaids Award for Best Relief Pitcher in the American League while playing for the Texas Rangers in 1979.  </p>

	<p>After retiring from baseball in 1986, he started Emu Outfitting, a company that organizes hunting, fishing, and photography trips in North and South America. Throughout the last 25 years, he’s served as managing director of operations for several peacock bass fishing lodges throughout Brazil's Amazon River region. He’s also guided hunters, fishermen, and photographers extensively in North and South America for 50 years, and he’s been fishing and guiding in Alaska since 1988.</p>

	<p>Jim first came to Rainbow Bay Resort in 2007 and managed daily operations for nearly four years. He then reprised his role as operations manager in January 2014 and continues to enjoy showing guests the wonders of Southwest Alaska—including its remote fishing experiences, natural history nuances, and abundance of variety when it comes to fishing, wildlife, and photography opportunities. He also has a knack for going above and beyond for clients. In other words, if a client can dream it, Jim can make it happen.</p>

	<p>When he isn’t in Alaska, you’ll find him spending time with his family in Texas, perfecting his award-winning photography skills, and organizing charity events to support area causes, just to name a few. </p>
        
