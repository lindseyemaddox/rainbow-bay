  <h2 property="jobTitle">Fishing Guide</h2>

  <img src="/_assets/img/staff-baldwin.jpg" alt="Angry Eagle fishing guide Ken Baldwin">

  	<p>Ken Baldwin joined the Angry Eagle family in 2003, when he was invited to visit for a summer. Since then, Ken has been returning every year to share his years of fishing and outdoor knowledge with guests.

	<p>“I can’t even remember when I started fishing,” said Ken, who has enjoyed the sport from a young age. “I remember my first four-pound bass, though. I caught it on a black and yellow wooden Hula Popper with feathers. It was before the days of 'catch and release,' so I proudly carried the fish on a stringer to my dad’s Rod and Gun Club. One of the members slipped a $5 bill into my pocket, wanting to hear how I caught it. I was 10 years old and felt like I had just been accepted into the bass fraternity.”</p>

	<p>And while bass fishing remains his first love, the truth is he’s never met a fish he didn’t like. “I’d fish a puddle with a chopstick if I thought I could catch something,” he said.</p>

	<p>When Ken isn’t guiding in Alaska, you’ll most likely find him traveling and fishing. Some of his favorite experiences have been fishing in Charleston, South Carolina; Lake El Salto, Mexico; Montauk, New York; the Rio Negro in Brazil; Venice, Louisiana; and Costa Rica. </p>

	<p>As a fishing guide, his first priority is safety, but he also knows the importance of helping provide an all-around amazing experience. That’s why Ken gives personalized fishing instruction based on guests’ various skill levels. He wants clients to not only catch fish, but also to leave better fishermen from when they first arrived.</p>

	<p>Another one of his goals is for guests to know that visiting Alaska is about more than just fishing.</p>

	<p>“It’s easy to get caught up in the pursuit of fish, but sometimes we need to stop and take in the beauty of Alaska,” Ken said. “It’s truly majestic country and should be fully experienced. There are moments out here that will touch your heart.”</p>

	<p>Fishing isn’t Ken’s only talent. He’s happy to teach clients about photography—including how to take, edit, and enhance photos. And when he isn’t scouting his next big catch or capturing the great outdoors on film, you can find him hosting his TV show “Season on the Edge,” which focuses on traveling, fishing, and exploring interesting places near and far. </p>

