  <p>Erin Rogers was just 5 years old when his grandfather inspired his lifelong love of fishing—which ultimately led him to dedicate his career to sharing his passion with others. </p>

  <p>“My grandfather was the biggest influence in my life,” he said. “My life of fishing is all his fault. I am so lucky to be able to do what I love every day.”</p>

  <p>In 1998, Erin joined Angry Eagle as a fishing guide, where he continues to be an instrumental part of the team. After spending years mastering fishing techniques, he makes it his goal to help all clients gain that same knowledge and confidence as fishermen. Whether he’s teaching fly fishing fundamentals or the finer points of casting, Erin is dedicated to instructing clients of all skill levels.</p>

  <p>When Erin isn’t at Angry Eagle, you'll find him enjoying time with his son, guiding hunters in Alaska for brown bear and moose, or guiding fishermen on Lake El Salto, a world-renowned Mexican lake known for its largemouth bass. He’s also been privileged to guide several big names in the fishing industry, including Forrest Wood, founder of Ranger Boats, and has filmed with Outdoor Magazine’s Hank Parker and been featured in fellow Angry Eagle fishing guide Ken Baldwin’s TV show, “Season on the Edge.”</p>

