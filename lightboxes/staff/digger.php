  	<p>Just the like weather in Southwest Alaska, the menu at Angry Eagle Lodge & Outfitters is constantly changing. And it’s all thanks to Chef Daniel “Digger” Black, who ensures Angry Eagle guests are treated to a rich selection of inventive dishes that focus on flavor and the finest ingredients.</p>

  	<p>Chef Digger first began cooking with his grandmother when he was just 8 years old, and his passion for good food can be savored in every meal he creates. Whether it’s his homemade pickled vegetables, house-smoked cherry wood bacon and sausage, or signature sockeye salmon served on a cedar plank, he’s mastered the art of combining basic ingredients to create unforgettable food experiences.</p>

  	<p>Bringing more than 10 years of culinary experience to Angry Eagle, he’s spent the last six years working exclusively at lodges. He describes his style of cooking as simple and creates dishes that include everything from house-smoked prime rib with creamed horseradish and three-cheese, twice-baked potatoes to Alaskan king crab and braised short ribs with a red wine veal demi-glace.</p>

  	<p>He devotes about 10 hours a day to ensuring guests are well fed from the time they leave the lodge each morning to the time they return after a long day spent fishing. When he’s not preparing meals, he’s creating new dishes, perfecting his current offerings, or baking an assortment of impressive desserts. As you might imagine, Chef Digger doesn’t usually have a lot of free time to go fishing, but he’s definitely had his share of impressive catches—including a 300-pound halibut from Kukak Bay, Alaska.</p>

