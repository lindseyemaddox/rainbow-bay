  <h1>Lake Trout</h1>

        <div class="description">
          <p><span>What: </span>Also known as “lakers,” lake trout are big-water fish typically found in the deep waters of large lakes. These fish seldom jump and are hard fighters capable of making long, strong runs. <span style="font-style: italic; color:#504E3C; font-weight: light;">(These fish are actually part of the char family. Any “trout”-type fish with white on the leading edge of its fins is actually part of the char family.)</span></p>
          <p><span>When: </span>These fish can be found from late July to early September, as they move from deep lake waters into the shallow waters around the mouth of streams to spawn.</p>
          <p><span>How: </span>Lake trout are typically found near the mouths of the streams flowing into lakes and caught with spinners, jigs, and streamers. Quite often, lake trout are caught while grayling fishing at the mouths of the streams that flow into Lake Clark and Lake Kontrashabona, both of which are in Lake Clark National Park.</p>
